# write a program to accept elements is the form of a tuple and display its min,max,sum and avg.

# creating tuple
tuple_ex = (1,2,7,4,4,7,1,3,3,4,4,0)

# min value
print("The min value is = ", min(tuple_ex))

# max value
print("The max value is = ",max(tuple_ex))

# sum value
print("The sum is = " ,sum(tuple_ex))

# avg value
print("The average is = ",sum(tuple_ex)/len(tuple_ex))